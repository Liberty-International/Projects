                        The value of this type of bond is set by the port director but is generally not less than the total entered value plus all duties, taxes and fees. If the
                        goods are subject to the requirements of other government agencies the bond amount would be not less than 3 times the total entered value of the
                        merchandise.
                    </p>
                    <p>
                        <strong>A Continuous Bond is generally obtained by importers who have more than 1 or 2 entries and ISF’s per year.</strong>
                        Liberty International can estimate the amount of the bond, but ultimately the Revenue Division of CBP's National Finance Center sets the amount of each bond.
                        The minimum bond amount for a continuous bond is the greater of either $50,000.00 or 10% of the total amount of duties, taxes and fees paid to CBP in the previous 12 month
                        period.
